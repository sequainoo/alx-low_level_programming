/**
 * largest_number - returns the largest of 3 numbers
 * @a: first integer
 * @b: second integer
 * @c: third integer
 * Return: largest number
 */

int largest_number(int a, int b, int c)
{
	int largest;

	a < b ? largest = b : largest = a;
	largest < c ? largest = c : largest = largest;

	return (largest);
}
